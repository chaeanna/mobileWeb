//Number type
let n1 = 1234;
let n2 = 5.678;
console.log(n1);
console.log(n2);
console.log(typeof(n2));

//String type
let s1 = "hello";
let s2 = "world";
console.log(s1);
console.log(s2);
console.log(typeof(s1));

//Boolean type
let b1 = true;
let b2 = false;
console.log(b1);
console.log(b2);
console.log(typeof(b2));

//Null type
let n= null;
console.log(n);
console.log(typeof(n));

//Undefined type
let u1;
let u2 = undefined;
console.log(u1);
console.log(u2);
console.log(typeof(u2));

//Array type
let arr = [1,2,3,4];
console.log(arr);
console.log(typeof(arr));

//Object type
let obj = { a: "apple", b: "banana", c: "carrot" };
console.log(obj);
console.log(typeof(obj));